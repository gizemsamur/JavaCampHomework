package javakamp;

public class student {
	
	int id;
	String studentName;
	String lastName;
	String email;
	String address;
	
	public student(int id,String studentName,String lastName,String email,String address) {
		this.id=id;
		this.studentName=studentName;
		this.lastName=lastName;
		this.email=email;
		this.address=address;
	}

}
